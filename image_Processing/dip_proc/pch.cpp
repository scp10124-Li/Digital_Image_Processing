// pch.cpp: 對應到先行編譯標頭的來源檔案

#include "pch.h"

extern "C" {
	//===========================================================================
	//
	//===========================================================================
	__declspec(dllexport) void encode(int *f, int w, int h, int *g)
	{
		for (int i = 0; i < w * h; i++)
				g[i] = 255 - f[i];

	}
}